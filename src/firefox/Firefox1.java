public class Firefox1 {

	public static void main(String[] args)
	{

System.out.println("Cross browser testing");

	//*
    //**
    //***
    //****
    //*****
    
        // star-->5 (++),
 		int star=1;
		for(int row=1;row<=5;row++)
		{
			for(int col=1;col<=star;col++)
			{
				System.out.print("*");
				
			}
			System.out.println();
			star++;
		}
		
	
//===============================================
		int stars=5;
		for(int row=1;row<=5;row++)
		{
			for(int col=1;col<=stars;col++)
			{
				System.out.print("*");
				
			}
			System.out.println();
			stars--;
		}

		
		
	}

}
